#Assessing Logic
"""
Operator:	Operations:
and			Logical AND
or			Logical OR
not			Logiocal NOT
"""

a=True
b=False

#AND evalutions
print('AND Logic:')
print('a and a =', a and a)
print('a and b =', a and b)
print('b and b =', b and b)

#OR evalutions
print('\nOR Logic:')
print('a or a =', a or a)
print('a or b =', a or b)
print('b or b =', b or b)

#NOT evalutions
print('\nNOT Logic:')
print('a =', a,'\tnot a =', not a)
print('a =', b,'\tnot b =', not b)